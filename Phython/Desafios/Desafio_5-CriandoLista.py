# Pedir ao usuário que digite dois numeros
num1 = float(input('Devera escolher dois numeros, qual o primeiro? '))
num2 = float(input('Qual o segundo? '))

# Realizar os calculos matematicos
sum = num1+num2
subtraction = num1-num2
multiplication = num1*num2
division = num1/num2
exponentiation = num1**num2

# Imprimindo os resultados na console
print(f'Soma: {sum}')
print(f'Subtração: {subtraction}')
print(f'Multiplicação: {multiplication}')
print(f'Divisão: {division}')
print(f'Exponenciação: {exponentiation}')
